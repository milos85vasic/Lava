// TestNewGenerator_BasicConstruction verifies the constructor wires the
// outputDir correctly and does NOT touch the filesystem at construction.
func TestNewGenerator_BasicConstruction(t *testing.T) {
	dir := filepath.Join(t.TempDir(), "subdir-does-not-exist-yet")
	g := NewGenerator(dir)

	if got, want := g.OutputDir(), dir; got != want {
		t.Errorf("OutputDir() = %q; want %q", got, want)
	}
	// Constructor must not have created the directory.
	if _, err := os.Stat(dir); !os.IsNotExist(err) {
		t.Errorf("constructor created outputDir %q; expected not-yet-exists", dir)
	}
}

// TestGenerateClosureLog_HappyPath is the primary anti-bluff assertion
// (per §6.J.3): the file MUST exist at the expected §6.O path with the
// expected §6.O schema sections. Mutating any of the rendered headings
// makes this test fail with a clear assertion.
func TestGenerateClosureLog_HappyPath(t *testing.T) {
	dir := t.TempDir()
	g := NewGenerator(dir)

	in := fullClosureInput()
	path, err := g.GenerateClosureLog(context.Background(), in)
	if err != nil {
		t.Fatalf("GenerateClosureLog error: %v", err)
	}

	// Filename matches §6.O pattern: <YYYY-MM-DD>-<slug>.md
	wantName := "2026-05-14-rutracker-loginusecase-unknown.md"
	if got := filepath.Base(path); got != wantName {
		t.Errorf("filename = %q; want %q", got, wantName)
	}

	// File exists on disk (user-visible state).
	info, err := os.Stat(path)
	if err != nil {
		t.Fatalf("written file not found: %v", err)
	}
	if info.Size() == 0 {
		t.Fatalf("written file is empty: %s", path)
	}

	content, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read written file: %v", err)
	}
	s := string(content)

	// §6.O schema required sections.
	required := []string{
		"# Crashlytics issue a29412cf6566d0a71b06df416610be57 — closure log",
		"**Issue ID:** `a29412cf6566d0a71b06df416610be57`",
		"**Title:** `lava.tracker.rutracker.domain.LoginUseCase.invoke`",
		"**Type:** FATAL",
		"**Device:** Samsung Galaxy S23 Ultra (SM-S918B), Android 16",
		"**Version:** 1.2.8 (1028) release — 1 event 2026-05-07",
		"**State at closure:** OPEN (operator marks closed)",
		"**Closure date:** 2026-05-14",
		"**Fix commit:** `abc1234deadbeef`",
		"## Root cause",
		"## Fix",
		"## Validation test (per §6.O + §6.AB)",
		"## Challenge Test (per §6.O + §6.AE)",
		"## Closure protocol",
		"Generated by lava-api-go/internal/qa/ticket",
	}
	for _, want := range required {
		if !strings.Contains(s, want) {
			t.Errorf("rendered markdown missing required fragment %q\nfull output:\n%s", want, s)
		}
	}

	// Body content actually included (not just headings).
	bodyChecks := []string{
		"LoginUseCase handles 4 cases",
		"RuTrackerNetworkApi.login wraps invoke",
		"RuTrackerNetworkApiLoginUnknownRegressionTest.kt",
		"Operator marks issue closed in Firebase Console",
	}
	for _, want := range bodyChecks {
		if !strings.Contains(s, want) {
			t.Errorf("rendered markdown missing required body content %q", want)
		}
	}
}

// TestGenerateClosureLog_RejectsEmptyCrashlyticsID is the §6.J posture
// against silently writing a file that claims to close an unidentified
// issue. The error MUST be ErrEmptyCrashlyticsID (so callers can match
// it) AND no file MUST be written.
func TestGenerateClosureLog_RejectsEmptyCrashlyticsID(t *testing.T) {
	dir := t.TempDir()
	g := NewGenerator(dir)

	in := fullClosureInput()
	in.CrashlyticsID = ""

	path, err := g.GenerateClosureLog(context.Background(), in)
	if !errors.Is(err, ErrEmptyCrashlyticsID) {
		t.Errorf("err = %v; want %v", err, ErrEmptyCrashlyticsID)
	}
	if path != "" {
		t.Errorf("path = %q; want empty on error", path)
	}
	// No file should have been written.
	entries, _ := os.ReadDir(dir)
	if len(entries) != 0 {
		t.Errorf("found %d files in output dir after rejected input; want 0", len(entries))
	}
}

