// TestGenerateClosureLog_SlugSanitization confirms the slug normalizer
// strips unsafe filename characters consistently across uppercase + spaces
// + special chars.
func TestGenerateClosureLog_SlugSanitization(t *testing.T) {
	tests := []struct {
		name     string
		inSlug   string
		wantName string
	}{
		{"lowercase passthrough", "abc-def", "2026-05-16-abc-def.md"},
		{"uppercase folded", "ABC-DEF", "2026-05-16-abc-def.md"},
		{"spaces to hyphens", "abc def ghi", "2026-05-16-abc-def-ghi.md"},
		{"special chars stripped", "abc!@#$%^&def", "2026-05-16-abc-def.md"},
		{"collapsed multi-hyphens", "abc---def", "2026-05-16-abc-def.md"},
		{"trimmed edge hyphens", "---abc---", "2026-05-16-abc.md"},
		{"underscore to hyphen", "abc_def_ghi", "2026-05-16-abc-def-ghi.md"},
		{"digits preserved", "abc123def", "2026-05-16-abc123def.md"},
	}
	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			dir := t.TempDir()
			g := NewGenerator(dir)
			in := fullClosureInput()
			in.Slug = tc.inSlug
			in.Date = time.Date(2026, 5, 16, 0, 0, 0, 0, time.UTC)
			path, err := g.GenerateClosureLog(context.Background(), in)
			if err != nil {
				t.Fatalf("err: %v", err)
			}
			if got := filepath.Base(path); got != tc.wantName {
				t.Errorf("filename = %q; want %q", got, tc.wantName)
			}
		})
	}
}

