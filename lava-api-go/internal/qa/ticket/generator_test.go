// TestGenerateClosureLog_ConcurrentSafe stress-tests the mutex around the
// wrapped HelixQA Generator. The HelixQA Generator's counter is not
// goroutine-safe; the adapter wraps the call with a mutex. Without the
// mutex this test surfaces a data race under `-race`.
func TestGenerateClosureLog_ConcurrentSafe(t *testing.T) {
	dir := t.TempDir()
	g := NewGenerator(dir)

	const n = 20
	var wg sync.WaitGroup
	for i := 0; i < n; i++ {
		wg.Add(1)
		go func(i int) {
			defer wg.Done()
			in := fullClosureInput()
			in.Slug = "concurrent-" + string(rune('a'+(i%26)))
			in.Date = time.Date(2026, 5, 16, 0, 0, 0, 0, time.UTC)
			in.CrashlyticsID = "id-" + in.Slug
			if _, err := g.GenerateClosureLog(context.Background(), in); err != nil {
				t.Errorf("concurrent #%d error: %v", i, err)
			}
		}(i)
	}
	wg.Wait()

	// At least 26 distinct files (n>26 collides on slug after mod, but
	// the file write is overwrite-safe — what we're checking is "no
	// panic, no race, all goroutines completed").
	entries, _ := os.ReadDir(dir)
	if len(entries) < 1 {
		t.Errorf("expected at least one file written; got %d", len(entries))
	}
}

